#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    for (int i = 0; i < 10; i++)
    {
        cout << "Array[" << i << "] =" << a[i] << endl;
    }

    return 0;
}



